H("4. Feature engineering: why, how, and impact", 1)
P("Motivation. The 11 base features are sufficient for the instantaneous targets (torque/forces/activations) but "
  "NOT for fatigue, which is cumulative: at the same instantaneous pose, a muscle is more fatigued late in the "
  "exercise than early on. A per-frame model given only instantaneous kinematics cannot distinguish these cases. "
  "We therefore engineered features that (a) expose the geometry/gravity non-linearities explicitly and (b) "
  "encode the HISTORY required for fatigue.")
